local vector = require("lib.hump.vector-light")
local rects = require("src.rects")

function genLineToOrbit(x, y, track)
    local ax, ay, radius, delta = unpack(rects.atom)
    local dx, dy = vector.sub(x, y, ax, ay)
    local length = vector.len(dx, dy)
    local orbit = radius - ((track-1) * delta)
    local odx, ody = vector.trim(length - orbit, dx, dy)
    local ox, oy = vector.sub(x, y, odx, ody)
    return {{x, y}, {ox, oy}}
end

